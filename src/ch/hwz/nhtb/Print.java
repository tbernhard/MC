package ch.hwz.nhtb;

public class Print extends Message {

	public boolean validate() {
//		Zum testen wie gross der Nachrichtentext und der Anhang ist.
//		System.out.println("Grösse der MMS Nachricht ist: " + this.getMessage().length() + " Byte.");
		boolean b = false;
		System.out.println("Validiere Print: ");
		try {
			long size = this.getMessage().length();
			if (size > 5242880) {
				System.out.println("Validierung NOK: Grösse von 5MB Anhang überschritten.");
			} else {
				b=!b;
				System.out.println("Validierung OK");
			}
		} catch (Throwable t) {
			System.out.println("Print Null Pointer Exception. Keine Nachricht übergeben");
		}
		return true;
	}

	@Override
	public void print() {
		System.out.println("Print wurde gesendet. \nAn Empfänger: " + this.getRecipient() + "\nNachrichtentext:" + this.getMessage());

	}
}
